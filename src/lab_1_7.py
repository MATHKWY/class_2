def insertionSort(list):
  for i in range(1,len(list)):
    key = list[i]

    j = i-1

    while j >= 0 and key < list[j]:
      list[j+1] = list[j]
      j-=1
    list[j+1] = key

list3 = [i for i in range(10000)]
